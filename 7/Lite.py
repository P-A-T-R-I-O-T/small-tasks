# которая возвращает сумму всех четных элементов списка.


def sum_even_elements(lst):
    return print(sum(x for x in lst if x % 2 == 0))

# Пример использования
numbers = [1, 2, 3, 4, 5, 6, 30, 100]
sum_even_elements(numbers)




