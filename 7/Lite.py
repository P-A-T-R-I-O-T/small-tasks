# то функция возвращает "Ошибка!"

def shut_down(a):
    if a == 'ДА':
        b = "Выключение"
    elif a == 'НЕТ':
        b ="Выключение отклонено"
    else:
        b = "Ошибка!"
    return (print(b))
a = input("Нужно ли выключить компьютер? ")
shut_down(a)

